//Concatenation
//Both numbers are added
console.log(3 + 5);

//Both strings are added
console.log("Neema" + "K" + "R");

//If you put a number in quotes, the rest of the numbers will be treated as strings, and concatenated.
console.log("5" + 2 + 3);

//If you declare first numbers and then strings the numbers get added and strings will be added
console.log(2 + 3 + "5");

//Adding number with string
console.log(5 + "Neema");

console.log(9 + 2 + "k");